# оперция сравнения 

x = -3
y = 3

# оперция "меньше"
# мы спрашиваем "значение x меньше значения y?"
res = x < y 

# операция "меньше либо равно" 
# мы спрашиваем "значение x меньше или равно значение y?"
res = x <= y 

# оперция "больше"
# мы спрашиваем "значение x больше значения y?"
res = x > y 

# операция "больше либо равно" 
# мы спрашиваем "значение x больше или равно значение y?"
res = x >= y 

# операция равенства (равно)
# мы спрашиваем "значение x равно значения y?"
res = x == y 

# операция НЕравенства ( не равно)
# мы спрашиваем "значение x НЕ равно значения y?"
res = x != y 

