# пример простого калькулятора 

value_1 = int(input("Введите 1 число: "))
value_2 = int(input("Введите 2 число: "))

operator = input("Введите операцию: ")

if operator == '+':
    result = value_1 + value_2
elif operator == '-':
    result = value_1 - value_2
elif operator == '*':
    result = value_1 * value_2
elif operator == '/':
    result = value_1 / value_2
else:
    result = "оператор не распознан :("

print(f"Результат = {result}")